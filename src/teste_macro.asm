SWAP: MACRO X, Y
    LOAD X
    STORE TEMP
    LOAD Y
    STORE X
    LOAD TEMP
    STORE Y
ENDMACRO

DOUBLE: MACRO VAL
    LOAD VAL
    ADD VAL
    STORE VAL
ENDMACRO

